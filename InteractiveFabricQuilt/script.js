function displayInfo(boxNumber) {
    const infoBox = document.getElementById('info-box');
    const selectedBox = boxInfo[boxNumber];

    // First, check if the info box is currently visible and showing the same content
    if (infoBox.style.display === 'block' && infoBox.dataset.activeBox === String(boxNumber)) {
        // If the same box is clicked again, hide the info box
        infoBox.style.display = 'none';
        infoBox.style.opacity = 0;
        infoBox.dataset.activeBox = ''; // Clear the active box data attribute
    } else {
        // Otherwise, update the content and show the info box
        if (selectedBox) {
            infoBox.innerHTML = selectedBox.info || "No information available for this box.";
        } else {
            infoBox.innerHTML = "No information available for this box.";
        }
        infoBox.style.display = 'block';
        infoBox.style.opacity = 1;
        infoBox.dataset.activeBox = String(boxNumber); // Set the current active box
    }
}


// Load the fabric names on hover
function loadFabricNames() {
    for (let i = 1; i <= 9; i++) {
        const gridBox = document.getElementById(`box${i}`);
        const fabricName = document.createElement('span');
        fabricName.textContent = boxInfo[i].name;
        gridBox.appendChild(fabricName);
        gridBox.setAttribute('onclick', `displayInfo(${i})`);  // Attach click event
    }
}
window.onload = function() {
    loadFabricNames();
};